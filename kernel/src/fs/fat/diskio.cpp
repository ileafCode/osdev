uint32_t get_fattime()
{
	time_t time = rtcGetTime();
	return (DWORD)(time.year - 80) << 25 |
           (DWORD)(time.month + 1) << 21 |
           (DWORD)time.weekday << 16 |
           (DWORD)time.hour << 11 |
           (DWORD)time.minute << 5 |
           (DWORD)time.second >> 1;
}
